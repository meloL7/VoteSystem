package com.elvis.vote.pojo;

public class Power {
    private int id;
    private int voter_sno;
    private int colleage_id;
    private int major_id;
    private int grade_id;
    private int classes_id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getVoter_sno() {
        return voter_sno;
    }

    public void setVoter_sno(int voter_sno) {
        this.voter_sno = voter_sno;
    }

    public int getColleage_id() {
        return colleage_id;
    }

    public void setColleage_id(int colleage_id) {
        this.colleage_id = colleage_id;
    }

    public int getMajor_id() {
        return major_id;
    }

    public void setMajor_id(int major_id) {
        this.major_id = major_id;
    }

    public int getGrade_id() {
        return grade_id;
    }

    public void setGrade_id(int grade_id) {
        this.grade_id = grade_id;
    }

    public int getClasses_id() {
        return classes_id;
    }

    public void setClasses_id(int classes_id) {
        this.classes_id = classes_id;
    }
}
